export function getLesson2Part(partNumber: number): Lesson2Part | null {
  return LESSON_2_PARTS.find((p) => p.partNumber === partNumber) || null;
}

export function getLesson2Question(
  partNumber: number,
  questionIndex: number
): string | null {
  const part = getLesson2Part(partNumber);
  if (!part) return null;
  if (questionIndex < 1 || questionIndex > part.questions.length) return null;
  return part.questions[questionIndex - 1];
}

export function getLesson2PartQuestionCount(partNumber: number): number {
  const part = getLesson2Part(partNumber);
  return part ? part.questions.length : 0;
}

export function getTotalLesson2Questions(): number {
  return LESSON_2_PARTS.reduce((sum, part) => sum + part.questions.length, 0);
}

export function generateLesson2Context(
  partNumber: number,
  questionIndex: number
): string {
  const part = getLesson2Part(partNumber);
  if (!part) return "";

  const question = getLesson2Question(partNumber, questionIndex);
  const totalInPart = part.questions.length;

  return `
────────────────────────────────
CURRENT POSITION
────────────────────────────────
You are in PART ${partNumber} (${part.name}).
Current question: ${questionIndex} of ${totalInPart} in this PART.

Your next question to ask is:
"${question}"

Ask this question now. Wait for the student's response.
After the student responds, the system will tell you the next question.
`;
}
